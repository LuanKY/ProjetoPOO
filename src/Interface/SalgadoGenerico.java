    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Estoque e = new Estoque();
        int quantidade = Integer.parseInt(jTextFieldqt.getText());
        double valor = Double.parseDouble(jTextFieldvl.getText());
        JOptionPane.showMessageDialog(null, e.CadastrarProduto(quantidade, jFormattedTextFieldnm1.getText(), (float) valor, "SALGADO"));

        try {        
            FileWriter arq = new FileWriter("estoque.dat",true);
            PrintWriter pArq = new PrintWriter(arq);
            
            pArq.println(jFormattedTextFieldnm1.getText());
            pArq.println(quantidade);
            
            pArq.flush();
            pArq.close();
            arq.close();
            
        } catch (IOException ex) {
            Logger.getLogger(Cliente.class.getName()).log(Level.SEVERE, null, ex);
        }

        
        jFormattedTextFieldnm1.setText("");
        jTextFieldqt.setText("");
        jTextFieldvl.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

